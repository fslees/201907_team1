#include "ratingmanager.h"
//*****************************************************************************
// マクロ定義
//*****************************************************************************
#define	HITQUALITY_MAX	(10)

//*****************************************************************************
// コンストラクタ
//*****************************************************************************
RatingManager::RatingManager()
{
	rating = new Rating[HITQUALITY_MAX];
}

//*****************************************************************************
// ゲームの終了
//*****************************************************************************
RatingManager::~RatingManager()
{
	delete[] rating;
}

//*****************************************************************************
// ゲームの更新
//*****************************************************************************
void RatingManager::Update()
{
	for (int i = 0; i < HITQUALITY_MAX; i++)
	{
		if (rating[i].use)
		{
			rating[i].Update();
		}
	}
}

//*****************************************************************************
// リザルト画面の描画
//*****************************************************************************
void RatingManager::Draw()
{
	for (int i = 0; i < HITQUALITY_MAX; i++)
	{
		if (rating[i].use)
		{
			rating[i].Draw();
		}
	}
}

//*****************************************************************************
// ヒット時の評価表示の設置
//*****************************************************************************
void RatingManager::SetRaitng(int texNum)
{
	for (int i = 0; i < HITQUALITY_MAX; i++)
	{
		if (!rating[i].use)
		{
			rating[i].texNum = texNum;
			rating[i].use = true;

			break;
		}
	}
}
