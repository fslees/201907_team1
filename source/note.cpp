#include "note.h"


//=====================================
//コンストラクタ
//=====================================
Note::Note()
{
	ctrl = new NoteController();
	view = new NoteView();

	ctrl->note = this;
	view->note = this;
}

//=======================================
//デストラクタ
//=======================================
Note::~Note()
{
	delete ctrl;

	delete view;
}

//=======================================
//更新処理
//=======================================
void Note::Update()
{
	ctrl->Update();
}

//=======================================
//描画処理
//=======================================
void Note::Draw()
{
	view->Draw();
}